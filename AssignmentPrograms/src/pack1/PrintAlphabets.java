package pack1;

public class PrintAlphabets {

	 public static void main(String[] args) {
	       
	        printAlphabets();
	    }

	    public static void printAlphabets() {
	       
	        for (char c = 'a'; c <= 'z'; c++) {
	            System.out.print(c + " ");
	        }
	        System.out.println(); 
	    }

}
